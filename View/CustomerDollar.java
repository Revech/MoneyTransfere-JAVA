    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       
        Double pay = Double.parseDouble(in1.getText());
        pay = pay * 1500;
        if (in1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter amount of dollars to charge!");
        }
        else if (controller.getBalance(Login.activeCustomer) < pay) {
            JOptionPane.showMessageDialog(this, "You do not have enough balance, please recharge balance first!");
        } else {
            num.setVisible(true);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

