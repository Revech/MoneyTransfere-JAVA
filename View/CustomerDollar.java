    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
        if (inn.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter a phone number to transfer dollars!");
        } else {
            Double pay = Double.parseDouble(in1.getText());
            pay = pay * 1500;
             controller.addCustomerPurchace(Login.activeCustomer, controller.getCustomerNumber(Login.activeCustomer), in1.getText() + " $", pay);
             controller.reduceBalance(Login.activeCustomer, pay);
             JOptionPane.showMessageDialog(this, "The phone number: " + inn.getText() + " has recieved " + in1.getText() + "$!");
             this.dispose();
             OrderFrame o = new OrderFrame();
             o.setVisible(true);

        }


    }//GEN-LAST:event_jToggleButton3ActionPerformed

